package com.poc.h2.demo;

public class KRIList {
	
	private String kriName;
	private String kriType;
	private String kriAliasName;
	private String sheetNo;
	public String getKriName() {
		return kriName;
	}
	public void setKriName(String kriName) {
		this.kriName = kriName;
	}
	public String getKriType() {
		return kriType;
	}
	public void setKriType(String kriType) {
		this.kriType = kriType;
	}
	public String getKriAliasName() {
		return kriAliasName;
	}
	public void setKriAliasName(String kriAliasName) {
		this.kriAliasName = kriAliasName;
	}
	public String getSheetNo() {
		return sheetNo;
	}
	public void setSheetNo(String sheetNo) {
		this.sheetNo = sheetNo;
	}
	
	

}
